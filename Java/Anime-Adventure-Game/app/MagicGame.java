  /**
   * Handles the fight between the player and a monster.
   *
   * @param monster The monster to fight against.
   */
  public void fightingMonsters(Monster monster) {
    monster.getStats();
    monster.intro();
    System.out.println();

    while (player.isAlive && monster.lifePoints > 0 && monster.isAlive) {
      Spell[] spells = player.getSpells();
      System.out.println("Choose a Spell !!");

      String input = readUserInput();

      try {
        int intValue = Integer.parseInt(input);

        if (intValue < 1 || intValue > spells.length) {
          System.out.println("The input '" + input + "' is invalid. Please enter a valid number from the provided list.");
        } else {
          Spell pickedSpell = spells[intValue - 1];
          int playerDmg = player.attack(pickedSpell);
          monster.getDamaged(playerDmg);

          if (monster.lifePoints > 0) {
            int enemyDmg = monster.attack();
            player.getDamaged(enemyDmg);
          }
        }
      } catch (NumberFormatException e) {
        System.out.println("The input '" + input + "' is invalid. Please enter a valid number from the provided list.");
      }
    }

    if (!player.isAlive) {
      monster.winOutro();
      return;
    }
    if (monster.lifePoints <= 0) {
      monster.loseOutro();
      player.win(monster.getMoney());
    }
  }

  /**
   * Retrieves the available monsters for the player to fight.
   *
   * @return An array of available monsters.
   */
  public Monster[] getMonsters() {
    int listSize = 0;
    for (int i = 0; i < monsters.length; i++) {
      if (monsters[i] != null && monsters[i].isAlive) {
        listSize++;
      }
    }

    Monster[] availableMonsters = new Monster[listSize];
    int currIndex = 0;
    for (int i = 0; i < monsters.length; i++) {
      if (monsters[i] != null && monsters[i].isAlive) {
        availableMonsters[currIndex] = monsters[i];

        // Dynamically set level for the monster
        int randLevel = LevelGenerator.generateRandomLevel(player);
        availableMonsters[currIndex].setLevel(randLevel);
        currIndex++;
      }
    }

    return availableMonsters;
  }

  /**
   * Starts a new adventure in the game.
   */
  public void newAdventure() {
    // Game logic
    if (player.isAlive) {
      Monster[] availableMonsters = getMonsters();

      for (int i = 0; i < availableMonsters.length; i++) {
        if (availableMonsters[i].isAlive) {
          fightingMonsters(availableMonsters[i]);

          if (availableMonsters[i].lifePoints <= 0) {
            availableMonsters[i].setDeath();
          }
          if (availableMonsters[i].lifePoints <= 0 && availableMonsters.length == 1) {
            player.winGame();
            this.runningGame = false;
            MagicAdventureApp.gameRunning = false;
          }

          break;
        }
      }
    }

    if (!player.isAlive) {
      this.runningGame = false;
      MagicAdventureApp.gameRunning = false;
    }
  }

  /**
   * Handles the user input for the game menu and performs the corresponding action.
   *
   * @param input The user input representing the selected menu option.
   */
  private void handleInputGameMenu(String input) {
    switch (input) {
      case "1":
        this.newAdventure();
        break;
      case "2":
        this.getSpellsFromMW();
        break;
      case "3":
        this.getLearnedSpells();
        break;
      case "4":
        this.player.getStats();
        break;
      case "5":
        this.flipCoin();
        break;
      case "6":
        this.player.heal();
        break;
      case "7":
        runningGame = false;
        MagicAdventureApp.runningGameMainMenu();
        break;
      default:
        System.out.println("The input '" + input + "' is invalid. Please choose a correct number between 1 and 6");
        break;
    }
  }

